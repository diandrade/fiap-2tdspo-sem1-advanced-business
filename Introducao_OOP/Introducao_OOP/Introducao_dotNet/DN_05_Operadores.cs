namespace Introducao_OOP.Introducao_dotNet;

public class DN_05_Operadores
{
    public DN_05_Operadores()
    {
        int numero_1 = 15;
        int numero_2 = 5;
        
        int soma = numero_1 + numero_2;
        Console.WriteLine("Soma: {0}", soma);
        
        int subtracao = numero_1 - numero_2;
        Console.WriteLine("Subtracao: {0}", subtracao);
        
        int divisao = numero_1 / numero_2;
        Console.WriteLine("Divisao: {0}", divisao);
    }
}